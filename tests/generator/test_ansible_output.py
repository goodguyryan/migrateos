def test_render_j2_preserves_ansible_syntax(tmp_path):
    """_render_j2 does NOT interfere with Ansible {{ }} syntax inside templates.

    This is the critical invariant: Jinja2 renders its own {{ }} variables,
    but the template author must use {% raw %}...{% endraw %} blocks around
    Ansible variable references. This test verifies that pattern works.
    """
    template_dir = tmp_path / "templates"
    template_dir.mkdir()
    (template_dir / "task.yml.j2").write_text(
        "# {{ version_note }}\n"
        "- name: Install packages\n"
        "  ansible.builtin.dnf:\n"
        '    name: "{{ dnf_packages }}"\n'
    )

    destination = tmp_path / "output" / "task.yml"

    jinja2_environment = Environment(
        loader=FileSystemLoader(str(template_dir)),
        # Use Jinja2 block_start/block_end that don't conflict with Ansible
        # OR the template uses {% raw %} blocks.
        # For this test, we pass dnf_packages as a variable so Jinja2 resolves it.
    )
    template_variables = {
        "version_note": "Generated by MigrateOS v0.1.0",
        "dnf_packages": "nginx, postgresql",
    }

    render_j2(jinja2_environment, "task.yml.j2", destination, template_variables)

    content = destination.read_text()
    assert "Generated by MigrateOS v0.1.0" in content
    assert "nginx, postgresql" in content

