def test_apply_substitutions_basic():
    """Placeholders are replaced with values from template_variables."""
    content = (
        "# __VERSION_NOTE__\n"
        "# Source: __SOURCE_HOSTNAME__\n"
        "# __TIMESTAMP__\n"
        "- name: Install packages\n"
        "  ansible.builtin.dnf:\n"
        "    name: \"{{ dnf_packages }}\"\n"
    )
    template_variables = {
        "version_note": "Generated by MigrateOS v0.1.0 from webserver.local",
        "source_hostname": "webserver.local",
        "timestamp": "2026-06-21T14:30:00",
    }

    result = apply_substitutions(content, template_variables)

    assert "__VERSION_NOTE__" not in result
    assert "__SOURCE_HOSTNAME__" not in result
    assert "__TIMESTAMP__" not in result
    assert "Generated by MigrateOS v0.1.0 from webserver.local" in result
    assert "webserver.local" in result
    assert "2026-06-21T14:30:00" in result
    # Ansible {{ }} syntax must survive untouched
    assert "{{ dnf_packages }}" in result

