def test_generate_creates_full_structure(tmp_path, sample_manifest):
    """generate() creates all expected files and directories from a manifest."""
    manifest_path = tmp_path / "manifest.yml"
    save_yaml(sample_manifest, manifest_path)

    output_dir = tmp_path / "output"
    output_dir.mkdir()

    generate(manifest_path, output_dir)

    ansible_dir = output_dir / "generated-ansible"

    # Root files
    assert (ansible_dir / "ansible.cfg").exists()
    assert (ansible_dir / "requirements.yml").exists()
    assert (ansible_dir / "inventory.ini").exists()
    assert (ansible_dir / "site.yml").exists()
    assert (ansible_dir / "WARNINGS.md").exists()

    # Group vars
    assert (ansible_dir / "group_vars" / "new_server.yml").exists()
    assert (ansible_dir / "group_vars" / "vault.example.yml").exists()

    # Task files
    tasks_dir = ansible_dir / "roles" / "migrated_server" / "tasks"
    assert (tasks_dir / "main.yml").exists()
    assert (tasks_dir / "bootstrap.yml").exists()
    assert (tasks_dir / "packages.yml").exists()

    # Handlers
    assert (tasks_dir / "handlers" / "main.yml").exists()


