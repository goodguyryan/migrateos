def test_render_j2_creates_file(tmp_path):
    """_render_j2 renders a .j2 template and writes the output to disk."""
    template_dir = tmp_path / "templates"
    template_dir.mkdir()
    (template_dir / "test.cfg.j2").write_text("# {{ version_note }}\nhost_key_checking = False\n")

    output_dir = tmp_path / "output"
    output_dir.mkdir()
    destination = output_dir / "test.cfg"

    jinja2_environment = Environment(loader=FileSystemLoader(str(template_dir)))
    template_variables = {"version_note": "Generated by MigrateOS v0.1.0"}

    render_j2(jinja2_environment, "test.cfg.j2", destination, template_variables)

    assert destination.exists()
    content = destination.read_text()
    assert "Generated by MigrateOS v0.1.0" in content
    assert "host_key_checking = False" in content

