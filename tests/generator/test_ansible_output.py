def test_copy_task_files_creates_all_files(tmp_path):
    """copy_task_files reads template .yml files, applies substitutions, writes to dest."""

    template_dir = tmp_path / "templates" / "tasks"
    template_dir.mkdir(parents=True)
    tasks_dest = tmp_path / "output" / "roles" / "migrated_server" / "tasks"

    # Create 3 stub task files (subset of 15 for speed)
    (template_dir / "bootstrap.yml").write_text(
        "# __VERSION_NOTE__\n- name: Bootstrap\n  ansible.builtin.raw: echo ok\n"
    )
    (template_dir / "packages.yml").write_text(
        "# __VERSION_NOTE__\n- name: Install packages\n  ansible.builtin.dnf:\n    name: \"{{ dnf_packages }}\"\n"
    )
    (template_dir / "main.yml").write_text(
        "# __VERSION_NOTE__\n- name: Import tasks\n  ansible.builtin.import_tasks: bootstrap.yml\n"
    )

    template_variables = {
        "version_note": "Generated by MigrateOS v0.1.0 from webserver.local",
        "source_hostname": "webserver.local",
        "timestamp": "2026-06-21T14:30:00",
    }

    # Patch TASK_FILE_NAMES to only include our 3 stubs
    import src.generator.ansible_generator as gen
    original_names = gen.TASK_FILE_NAMES
    gen.TASK_FILE_NAMES = ["bootstrap.yml", "packages.yml", "main.yml"]
    try:
        copy_task_files(template_variables, template_dir, tasks_dest)
    finally:
        gen.TASK_FILE_NAMES = original_names

    assert (tasks_dest / "bootstrap.yml").exists()
    assert (tasks_dest / "packages.yml").exists()
    assert (tasks_dest / "main.yml").exists()

    content = (tasks_dest / "bootstrap.yml").read_text()
    assert "__VERSION_NOTE__" not in content
    assert "Generated by MigrateOS v0.1.0 from webserver.local" in content

