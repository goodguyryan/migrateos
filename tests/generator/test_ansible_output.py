def test_copy_bundled_files_copies_tree(tmp_path):
    """copy_bundled_files copies output_dir/files/ into the Ansible role."""

    output_dir = tmp_path / "bundler_output"
    (output_dir / "files" / "etc" / "nginx").mkdir(parents=True)
    (output_dir / "files" / "etc" / "nginx" / "nginx.conf").write_text("server {}\n")
    (output_dir / "files" / "systemd").mkdir(parents=True)
    (output_dir / "files" / "systemd" / "myapp.service").write_text("[Unit]\nDescription=myapp\n")

    ansible_dir = tmp_path / "generated-ansible"
    ansible_dir.mkdir()

    copy_bundled_files(output_dir, ansible_dir)

    assert (ansible_dir / "roles" / "migrated_server" / "files" / "etc" / "nginx" / "nginx.conf").exists()
    assert (ansible_dir / "roles" / "migrated_server" / "files" / "systemd" / "myapp.service").exists()
    assert (ansible_dir / "roles" / "migrated_server" / "files" / "etc" / "nginx" / "nginx.conf").read_text() == "server {}\n"


